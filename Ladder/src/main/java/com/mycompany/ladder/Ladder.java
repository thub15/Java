public class Ladder {

    public static void main(String[] args) {
        int x;
        
        for (x=0; x<10; x++) {
            
            switch (x) {
                case 0:
                    System.out.println("x is zero");
                    break;
                case 1:
                    System.out.println("x is one");
                    break;
                case 2:
                    System.out.println("x is two");
                    break;
                default:
                    System.out.println("x is two or more");
            }
            
            
            
            
            
            
         
        }
    }
}
